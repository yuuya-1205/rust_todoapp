//! ライブラリクレートの入口（ルートモジュール）。
//!
//! このプロジェクトは「バイナリ」と「ライブラリ」の2つの顔を持ちます。
//! - `src/main.rs` … 実行可能ファイル（バイナリクレート）の入口
//! - `src/lib.rs`  … ライブラリクレートの入口（このファイル）
//!
//! なぜライブラリを分けるのか？
//! `tests/` ディレクトリに置く「結合テスト」は、このパッケージ本体とは
//! **別のクレート** としてコンパイルされ、`todoapp::...` のように
//! ライブラリクレート経由で **公開API（`pub`）only** にアクセスします。
//! バイナリ（`main.rs`）だけの構成では `tests/` から中身を参照できないため、
//! モジュールの実体をこのライブラリ側に置いています。

/// データ構造（ドメイン）をまとめたモジュール。
pub mod model;
/// ユーザーとの接点（CLI）をまとめたモジュール。
pub mod presentation;
